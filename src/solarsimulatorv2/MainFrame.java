    private void graphButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_graphButtonActionPerformed
        int latitudeMultiplier,longitudeMultiplier,sMeridianMultiplier;
        double tempLat=0.0,
                tempLong=0.0,
                tempStandardMeridian=0.0;
        
        
        
        int latDir = latDirList.getSelectedIndex();
        int longDir = longDirList.getSelectedIndex();
        int stmDir = stmDirList.getSelectedIndex();
        if(latDir==0){
            latitudeMultiplier = 1;
        }else{
            latitudeMultiplier = -1;
        }
        if(longDir==0){
            longitudeMultiplier = -1;
        }else{
            longitudeMultiplier = 1;
        }
        if(stmDir==0){
            sMeridianMultiplier = -1;
        }else{
            sMeridianMultiplier = 1;
        }
        try{
            tempLat = Double.parseDouble(latitude.getText());
        }catch(Exception e){
            hasError =true;
            invalidLatitude =true;
        }
        
        try{
            tempLong = Double.parseDouble(longitude.getText());
        }catch(Exception e){
            hasError =true;
            invalidLongitude =true;
        }
        
        try{
            tempStandardMeridian = Double.parseDouble(stMeridian.getText());
        }catch(Exception e){
            hasError =true;
            invalidMeridian =true;
        }
        
        try{
            tilt = Double.parseDouble(tiltAngle.getText());
        }catch(Exception e){
            hasError =true;
            invalidTilt =true;
        }
        
        try{
            azimuth = Double.parseDouble(azAngle.getText());
        }catch(Exception e){
            hasError =true;
            invalidAzimuth =true;
        }
        try{
            cn1 = Double.parseDouble(cn.getText());
        }catch(Exception e){
            hasError = true;
            invalidCn = true;
        }
        try{
            rho1 = Double.parseDouble(rho.getText());
        }catch(Exception e){
            hasError = true;
            invalidRho = true;
        }
        try{
            month = dChooser.getDate().getMonth();
            date = dChooser.getDate().getDate();
        }catch(Exception e){
            noDateSelected = true;
            hasError = true;
        }
        //Display error Message if there are any.
        if(hasError){
//            message.setForeground(Color.red);
//            message.setText("INPUT ERROR : "+getErrorString());
            JOptionPane.showMessageDialog(this, "Your inputs contain some errors.\nPlease review and try again.", "Input Error", JOptionPane.ERROR_MESSAGE);
            hasError = false;
            canShowOther = false;
            genRad.setText("not available");
            radio1.setEnabled(false);
            moreButton.setEnabled(false);
            if(radio1.isSelected()){
                radio2.setSelected(true);
            }
        }
        //else calculate the output
        else{
            lat = tempLat*latitudeMultiplier;
            longi = tempLong*longitudeMultiplier;
            standardMeridian = tempStandardMeridian*sMeridianMultiplier;
            
            
            sunrise.setText("SR: "+calc.sunrise(month, date, lat, standardMeridian, longi));
            sunset.setText("SS: "+calc.sunset(month, date, lat, standardMeridian, longi));
            genRad.setText(df.format(calc.totalRad(standardMeridian,longi,lat,tilt,azimuth,cn1,rho1,month,date)));
            radio1.setEnabled(true);
            if(!radio1.isSelected()){
                radio1.setSelected(true);
            }
            
            canShowOther = true;
            dataset1 = createDataset(lat,longi,standardMeridian,tilt,azimuth,month,date,cn1,rho1);
            displayLineGraph(dataset1);
            moreButton.setEnabled(true);
            cPanel.repaint();
            
        }
    }//GEN-LAST:event_graphButtonActionPerformed
