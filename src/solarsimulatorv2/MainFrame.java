    private XYDataset createDataset(double lat,double longitude,double standardMeridian,double tilt,double azimuth,int month,int date,double cn,double rho) {
        
        final XYSeries series = new XYSeries("");

        for(int i=6;i<=18;i++){
            series.add(i, calc.insSolarRadiation(i, lat, longitude, standardMeridian, tilt, azimuth, month, date,cn,rho));
        }

        final XYSeriesCollection dataset1 = new XYSeriesCollection(series);
        return dataset1;
        
    }
    private JFreeChart createChart(final XYDataset dataset) {
        
        // create the chart...
        final JFreeChart chart = ChartFactory.createXYLineChart(
            "Radiation vs. Local Standard Time Graph",      // chart title
            "Local Standard Time (24-hours format)",                      // x axis label
            "Radiation (W/m^2)",                      // y axis label
            dataset,                  // data
            PlotOrientation.VERTICAL,
            false,                     // include legend
            true,                     // tooltips
            false                     // urls
        );

        // NOW DO SOME OPTIONAL CUSTOMISATION OF THE CHART...
        chart.setBackgroundPaint(new Color(0,102,102));
        chart.getTitle().setFont(new Font("Tahoma", java.awt.Font.BOLD, 16));
        chart.getTitle().setPaint(Color.white);
        

        
        // get a reference to the plot for further customisation...
        final XYPlot plot = chart.getXYPlot();
        plot.setBackgroundPaint(Color.black);
        plot.setOutlinePaint(Color.green);

        // plot.setAxisOffset(new Spacer(Spacer.ABSOLUTE, 5.0, 5.0, 5.0, 5.0));
        plot.setDomainGridlinePaint(Color.white);
        plot.setRangeGridlinePaint(Color.white);
        plot.getDomainAxis().setTickLabelPaint(Color.white);
        plot.getDomainAxis().setLabelPaint(Color.white);
        plot.getDomainAxis().setLabelFont(new Font("Tahoma", java.awt.Font.ITALIC, 13));
        plot.getRangeAxis().setTickLabelPaint(Color.white);
        plot.getRangeAxis().setLabelPaint(Color.white);
        plot.getRangeAxis().setLabelFont(new Font("Tahoma", java.awt.Font.ITALIC, 13));
        
        final XYLineAndShapeRenderer renderer = new XYLineAndShapeRenderer();
        renderer.setSeriesShapesVisible(0, true);
        renderer.setPaint(Color.green);
        
        plot.setRenderer(renderer);

        // change the auto tick unit selection to integer units only...
        final NumberAxis rangeAxis = (NumberAxis) plot.getRangeAxis();
        rangeAxis.setStandardTickUnits(NumberAxis.createIntegerTickUnits());
        // OPTIONAL CUSTOMISATION COMPLETED.
                
        return chart;
        
    }
    private PieDataset createPieData(double usage[],String appliance[]){
        DefaultPieDataset piedataset = new DefaultPieDataset();
        for(int i=0;i<usage.length;i++){
            if(appliance[i]!=null)
                piedataset.setValue(appliance[i], usage[i]);
        }
        return piedataset; 
    }
